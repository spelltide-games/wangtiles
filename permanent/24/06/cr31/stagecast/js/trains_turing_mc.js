// load buttons

function loadprog(a) {
	if (g_play == undefined) {play("t_"); }  // no board selected !!
	else {
	
		reset_loco();  // stop & reset
		
		// reset opacity of all tiles to 0
		for (var i=1; i<g_lazy_x.length; i++) {  // not loco tile
			var obj  = document.getElementById(g_play + i);
			obj.style.opacity = 0;  // hide over tile
		}	
		t_init();  // reset arrays & globals & flip some tiles  // not window!!
	}
	
	for (var i=0; i<a.length; i++) {  // flip these tiles
		if (a[i]<62) flip(a[i]);
		else man_flip(a[i]);
	}
}
